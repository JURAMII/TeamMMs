const notiReducer = (state, action) =>{
    console.log(action.data)
    switch(action.type){
        case 'create' :
        return{            
            inputs: Contents.inputs,
            datas: state.datas.concat(action.data)
        }
        case 'edit' :
        return{
            ...state,
            datas: state.datas.map((item)=>item.id === action.id?{...item, notiType:action.type, name:action.name, text:action.text}:item)
        }
        case 'remove' :
        return{
            ...state,
            datas: state.datas.filter((item)=>item.id!==action.id)
        }
        case 'search' :
        return{
            ...state,
            datas: state.datas.filter((item)=>item.name.includes(action.name)||item.text.includes(action.text))
        }
        default :
        return state;
        
    };
}

